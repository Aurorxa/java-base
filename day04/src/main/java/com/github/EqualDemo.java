package com.github;

public class EqualDemo {
    public static void main(String[] args) {
        // 使用加法赋值运算符
        int a = 3;
        a += 3; // a = a + 3
        System.out.println("a = " + a); // a = 6

        // 使用减法赋值运算符
        int b = 3;
        b -= 3; // b = b - 3
        System.out.println("b = " + b); // b = 0

        // 使用乘法赋值运算符
        int c = 3;
        c *= 3; // c = c * 3
        System.out.println("c = " + c); // c = 9

        // 使用除法赋值运算符
        int d = 3;
        d /= 3; // d = d / 3
        System.out.println("d = " + d); // d = 1

        // 使用取余赋值运算符
        int e = 3;
        e %= 3; // e = e % 3
        System.out.println("e = " + e); // e = 0
    }
}
