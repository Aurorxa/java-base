package com.github;

public class CompareDemo {
    public static void main(String[] args) {
        int a = 8;
        int b = 7;

        System.out.println("a > b 的结果是：" + (a > b)); // a > b 的结果是：true
        System.out.println("a >= b 的结果是：" + (a >= b)); // a >= b 的结果是：true
        System.out.println("a < b 的结果是：" + (a < b)); // a < b 的结果是：false
        System.out.println("a <= b 的结果是：" + (a <= b)); // a <= b 的结果是：false
        System.out.println("a == b 的结果是：" + (a == b)); // a == b 的结果是：false
        System.out.println("a != b 的结果是：" + (a != b)); // a != b 的结果是：true
    }
}
