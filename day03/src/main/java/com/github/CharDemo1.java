package com.github;

public class CharDemo1 {
    public static void main(String[] args) {

        char a = 'A';
        // a = A
        System.out.println("a = " + a);

        char b = '\u8bb8';
        // b = ä½ 
        System.out.println("b = " + b);

        char c = '\n';
        System.out.println("c = " + c);

        char d = 97;
        System.out.println("d = " + d);
    }
}
