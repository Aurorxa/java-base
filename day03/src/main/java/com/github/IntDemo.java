package com.github;

public class IntDemo {
    public static void main(String[] args){
        // byte
        byte b = 127;
        System.out.println("byte: " + b);
        // short
        short s = 32767;
        System.out.println("short: " + s);
        // int
        int i = 2147483647;
        System.out.println("int: " + i);
        // long
        long l = 9223372036854775807L;
        System.out.println("long: " + l);
    }
}
