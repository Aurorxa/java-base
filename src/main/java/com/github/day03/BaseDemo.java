public class BaseDemo {

    public static void main(String[] args) {

        // 二进制整数
        int num = 0b1010;

        // 十进制整数（默认）
        int num2 = 10;

        // 八进制整数
        int num3 = 012;

        // 十六进制整数
        int num4 = 0xA;

        System.out.println(num); // 10
        System.out.println(num2); // 10
        System.out.println(num3); // 10
        System.out.println(num4); // 10
    }
}
