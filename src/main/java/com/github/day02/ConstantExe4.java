package com.github.day02;

/**
 * 布尔常量的逻辑运算
 */
public class ConstantExe4 {
    public static void main(String[] args) {

        System.out.println(true); // true
        System.out.println(false); // false
        System.out.println(false); // false
        System.out.println(false); // false

        System.out.println(true); // true
        System.out.println(true); // true
        System.out.println(true); // true
        System.out.println(false); // false

        System.out.println(false); // false
        System.out.println(true); // true
    }
}
