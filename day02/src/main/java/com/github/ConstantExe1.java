package com.github;

/**
 * 整数常量的运算
 */
public class ConstantExe1 {
    public static void main(String[] args) {
        // 加法
        System.out.println(1 + 1);
        // 减法
        System.out.println(1 - 2);
        // 乘法
        System.out.println(2 * 2);
        // 除法
        System.out.println(1 / 2);
        // 取模
        System.out.println(1 % 2);
    }
}
