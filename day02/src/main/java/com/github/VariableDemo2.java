package com.github;

public class VariableDemo2 {
    public static void main(String[] args) {

        // 变量的初始化
        int age = 10;
        // 变量的初始化
        String name = "张三";
        // 变量的初始化
        double salary = 10000.0;

        // 输出变量 name 的值
        System.out.println("姓名是：" + name);
        // 输出变量 age 的值
        System.out.println("年龄是：" + age);
        // 输出变量 salary 的值
        System.out.println("工资是：" + salary);
    }
}
