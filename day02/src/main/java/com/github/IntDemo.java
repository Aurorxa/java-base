public class IntDemo {
    public static void main(String[] args) {

        // 定义 byte
        byte b = 100;
        System.out.println("byte: " + b);

        // 定义 short
        short s = 1000;
        System.out.println("short: " + s);

        // 定义 int
        int i = 10000;
        System.out.println("int: " + i);

        // 定义 long
        long l = 10L;
        System.out.println("long: " + l);
    }
}
