package com.github;

/**
 * 字符常量的运算
 */
public class ConstantExe3 {
    public static void main(String[] args) {
        // 转换为整数
        System.out.println((int) 'A'); // 65
        // 转换为整数
        System.out.println((int) 'B'); // 66
        // 计算 'B' 和 'A' 之间的距离
        System.out.println('B' - 'A'); // 1
    }
}
